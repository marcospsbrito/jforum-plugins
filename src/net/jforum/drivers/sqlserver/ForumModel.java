package net.jforum.drivers.sqlserver;

import net.jforum.entities.Forum;
import net.jforum.util.preferences.SystemGlobals;

/**
 * @author Andre de Andrade da Silva - andre.de.andrade@gmail.com
 * @version $Id: ForumModel.java,v 1.2 2005/02/03 12:37:45 rafaelsteil Exp $
 */
public class ForumModel extends net.jforum.drivers.generic.ForumModel
{
	/** 
	 * @see net.jforum.model.ForumModel#addNew(net.jforum.entities.Forum)
	 */
	public int addNew(Forum forum) throws Exception
	{
		this.setSupportAutoGeneratedKey(false);
		this.setAutoGeneratedKeysQuery(SystemGlobals.getSql("ForumModel.generatedForumId"));

		return super.addNew(forum);
	}
}
